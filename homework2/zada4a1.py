# Запрашиваем количество монеток у пользователя
n = int(input("Введите количество монеток: "))

# Создаем список для хранения сторон монеток
coins = []

# Заполняем список случайными значениями (0 - решка, 1 - герб)
import random
for i in range(n):
    coins.append(random.randint(0,1))

# Выводим список сторон монеток
print("Список монеток: ", coins)

# Считаем количество решек и гербов в списке
num_heads = coins.count(0)
num_tails = coins.count(1)

# Определяем, какая сторона встречается чаще
if num_heads > num_tails:
    # Если решек больше, то переворачиваем гербы
    flips = num_tails
    result = [0] * num_heads
else:
    # Если гербов больше, то переворачиваем решки
    flips = num_heads
    result = [1] * num_tails

