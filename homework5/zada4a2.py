def sum(a, b):
    # Базовый случай: если одно из чисел равно 0, возвращаем другое число
    if a == 0:
        return b
    if b == 0:
        return a
    
    # Рекурсивный случай: вычисляем сумму чисел a-1 и b+1
    return sum(a - 1, b + 1)

# Получаем входные числа от пользователя
a = int(input("Введите первое число: "))
b = int(input("Введите второе число: "))

# Вызываем функцию sum и выводим результат
result = sum(a, b)
print(f"Сумма: {result}")